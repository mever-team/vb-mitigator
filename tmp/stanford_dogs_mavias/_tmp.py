import csv


def count_unique_tags(csv_file):
    unique_tags = set()

    with open(csv_file, "r", encoding="utf-8") as file:
        reader = csv.reader(file)
        next(reader)  # Skip header

        for row in reader:
            _, tag = row  # Extract class and tag
            unique_tags.add(tag)

    return unique_tags


