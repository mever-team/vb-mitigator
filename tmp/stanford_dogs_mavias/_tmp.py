import csv
import ast


def count_unique_tags(csv_file):
    unique_tags = set()

    with open(csv_file, "r", encoding="utf-8") as file:
        reader = csv.reader(file)
        next(reader)  # Skip header

        for row in reader:
            _, tags_str = row  # Extract class and tags string
            tags = ast.literal_eval(tags_str)  # Convert string to list
            unique_tags.update(tags)

    return len(unique_tags)


